package percabangan;

public class Switch {
    public static void main(String[] args) {
        int Bejalanan=1;
        switch(Bejalanan){
            case 1:
                System.out.println("Urang Lapas");
                break;
            case 2:
                System.out.println("Urang Lapas Jua");
                break;
            case 3:
                System.out.println("Ubat Nyamuk");
                break;
            case 4:
                System.out.println("Dobel Det");
                break;
            default:
                System.out.println("Urang Halus");
                break;
        }
    }
}
