package operator;

public class OperatorEquality {
    public static void main(String[] args) {
        int i = 10;
        int j = 11;
        System.out.println(i==10);
        System.out.println(i==11);
        System.out.println(i==j);
        System.out.println(i>j);
        System.out.println(i<j);
        System.out.println(10>11);
        System.out.println(10<11);
        System.out.println(i!=10);
        System.out.println(j!=10);
        
    }
}
